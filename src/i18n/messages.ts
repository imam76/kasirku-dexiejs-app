export type Locale = keyof typeof messages;
export type TranslationKey = keyof typeof messages.id;
export type TranslationParams = Record<string, string | number>;

export const defaultLocale: Locale = 'id';
export const supportedLocales = Object.keys(messages) as Locale[];

export const translate = (
  locale: Locale,
  key: TranslationKey,
  params?: TranslationParams,
) => {
  const template: string = messages[locale][key] ?? messages[defaultLocale][key] ?? key;

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce<string>(
    (text, [paramKey, value]) => text.split(`{${paramKey}}`).join(String(value)),
    template,
  );
};
